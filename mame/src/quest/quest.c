static inline unsigned char inb( unsigned short usPort ) {

    unsigned char uch;
    
    asm volatile( "inb %1,%0" : "=a" (uch) : "Nd" (usPort) );
    return uch;
}


static inline void outb( unsigned char uch, unsigned short usPort ) {

    asm volatile( "outb %0,%1" : : "a" (uch), "Nd" (usPort) );
}


static void set_bios_mode_13h( void ) {

  int i;

  char sequencer[] = { 0x03, 0x01, 0x0F, 0x00, 0x0E };
  char attribute[] = { 0x41, 0x00, 0x0F, 0x03, 0x00 };
  char graphics[] = { 0x00, 0x00, 0x00, 0x00, 0x00, 0x40, 0x05, 0x0F, 0xFF };
  char crt[] = { 0x5F, 0x4F, 0x50, 0x82, 0x54, 0x80, 0xBF, 0x1F, 0x00, 0x41, 0x00, 0x00, 0x00, 0x00, 0x00, 0x96, 0x9C, 0x0E, 0x8F, 0x28, 0x40, 0x96, 0xB9, 0xA3, 0xFF };


  /* Setup sequencer register values */
  for( i = 0; i < 5; i++ ) {
    outb( i, 0x3C4 );
    outb( sequencer[i], 0x3C5 );
  }

  /* Setup atttribute controller register values */
  for( i = 0; i < 5; i++ ) {
    inb( STATUS_ADDR );		/* Reset flip-flop to index mode for next
				 * write to port 0x3C0 */
    outb( ( i + 0x10 ) | 0x20, 0x3C0 );
    outb( attribute[i], 0x3C0 );
  }

  /* Setup graphics controller register values */
  for( i = 0; i < 9; i++ ) {
    outb( i, 0x3CE );
    outb( graphics[i], 0x3CF );
  }

  /* Setup CRT controller register values */
  for( i = 0; i < 25; i++ ) {
    outb( i, 0x3D4 );
    outb( crt[i], 0x3D5 );
  }
  
  /* Misc output write register */
  outb( 0x63, 0x3C2 );
}


static void set_bios_mode_03h( void ) {

  int i;

  char sequencer[] = { 0x03, 0x00, 0x03, 0x00, 0x02 };
  char attribute[] = { 0x0C, 0x00, 0x0F, 0x08, 0x00 };
  char graphics[] = { 0x00, 0x00, 0x00, 0x00, 0x00, 0x10, 0x0E, 0x00, 0xFF }; 
  char crt[] = { 0x5F, 0x4F, 0x50, 0x82, 0x55, 0x81, 0xBF, 0x1F, 0x00, 0x4F, 0x00, 0x0E, 0x00, 0x00, 0x00, 0x50, 0x9C, 0x0E, 0x8F, 0x28, 0x1F, 0x96, 0xB9, 0xA3, 0xFF };
 
  /* Setup sequencer register values */
  for( i = 0; i < 5; i++ ) {
    outb( i, 0x3C4 );
    outb( sequencer[i], 0x3C5 );
  }

  /* Setup atttribute controller register values */
  for( i = 0; i < 5; i++ ) {
      inb( STATUS_ADDR );	/* Reset flip-flop to index mode for next
				 * write to port 0x3C0 */
      outb( ( i + 0x10 ) | 0x20, 0x3C0 );
      outb( attribute[i], 0x3C0 );
  }

  /* Setup graphics controller register values */
  for( i = 0; i < 9; i++ ) {
    outb( i, 0x3CE );
    outb( graphics[i], 0x3CF );
  }

  /* Setup CRT controller register values */
  for( i = 0; i < 25; i++ ) {
    outb( i, 0x3D4 );
    outb( crt[i], 0x3D5 );
  }
  
  /* Misc output write register */
  outb( 0x67, 0x3C2 );
}


/* This is used to change video mode -- for now, we use it to switch to
   224x288 */
static void update_video_mode( Register *regs, int length ) {

  int i;

  for( i = 0; i < length; i++ ) {
    if( regs[i].port == 0x3C2 )
      outb( regs[i].value, regs[i].port );
    else {
      if( regs[i].port == 0x3C0 ) {
	inb( STATUS_ADDR );
	outb(  regs[i].index | 0x20, regs[i].port );
	outb(  regs[i].value, regs[i].port );
      }
      else {
	outb( regs[i].index, regs[i].port );
	outb( regs[i].value, regs[i].port+1 );
      }
    }
  }
  /* Need bit 5 of attribute controller set to enable display */
  outb( 0x34, 0x3C0 );
  inb( 0x3C0 );			/* Need a pairwise set of ops on each port,
				   hence we do this call here */
}


