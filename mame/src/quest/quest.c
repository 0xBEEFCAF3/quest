int osd_key_pressed (int keycode) {
  
  int c;

  c = poll_keyboard();
  if( c & 0x80 )		/* Release key scan code */
    key[c & 0x7F] = 0;
  else
    key[c & 0x7F] = 1;

  return key[c & 0x7F];
}


/* Wait for a key press and return the keycode */
int osd_read_key( void ) {
  int c;
  do {
    c = poll_keyboard();
    if( c & 0x80 )		/* Release key scan code */
      key[c & 0x7F] = 0;
    else
      key[c & 0x7F] = 1;
  } while (c & 0x80);           /* while a "break" code */

  return c;
}


/* Create a bitmap. --??-- MSDOS version calls clearbitmap() allegro library
   routine to appropriately initialize it to the background color. */

struct osd_bitmap *osd_create_bitmap( int width,int height ) {

  struct osd_bitmap *bitmap;
  
  if ((bitmap = malloc( sizeof( struct osd_bitmap ) + 
			(height-1)*sizeof(unsigned char *))) != 0) {
    int i;
    unsigned char *bm;

    bitmap->width = width;
    bitmap->height = height;
    if ((bm = malloc( width * height * sizeof(unsigned char)) ) == 0) {
      free( bitmap );
      return 0;
    }

    for( i = 0;i < height;i++ )
      bitmap->line[i] = &bm[i * width];

    bitmap->private = bm;

    /* clearbitmap(bitmap); --??-- MSDOS calls this */
  }

  return bitmap;
}


void osd_free_bitmap( struct osd_bitmap *bitmap ) {

  if( bitmap ) {
    free( bitmap->private );
    free( bitmap );
  }
}


/* Create a display screen, or window, large enough to accomodate a bitmap
   of the given dimensions. Here, we force the resolution to be 224x288 by
   modifying VGA BIOS mode 13h
*/
struct osd_bitmap *osd_create_display( int width,int height ) {
  
  /* Set display register values temporarily for BIOS mode 13h: 320x200x256
     colours */
  set_bios_mode_13h();
  
  /* Now, modify settings to force 224x288 widthxheight */
  update_video_mode( scr224x288, sizeof( scr224x288 ) / sizeof( Register ));
  bitmap = osd_create_bitmap( width, height );
  
  return bitmap;
}


void osd_close_display( void ) {

  int i;
  unsigned char *buf = g_8x16_font;
  int pen = first_free_pen;
  first_free_pen = 0;

  for( i = 0; i < pen; i++ )
      osd_obtain_pen( old_red[ i ], old_green[ i ], old_blue[ i ] ); 

  /* Turn off even-odd addressing (set flat addressing) */
  outb( 0x04, 0x3C4 );		/* sequencer index 0x04 */
  outb( 0x06, 0x3C5 );		/* sequencer data 0x06 */

  /* Set plane 2 by first setting the read plane...*/
  outb( 0x04, 0x3CE );
  outb( 0x02, 0x3CF );
  /* ...and then setting the write plane */
  outb( 0x02, 0x3C4 );
  outb( 0x04, 0x3C5 );

  /* Here, we write the fontmap (for 8x16 fonts) to plane 2 of video RAM so
   * that we have valid fonts on return to text mode */
  for( i = 0; i < 256; i++ ) {
    memcpy( (void *)( 0x200000 + i * 32 ), buf, 16 );
    buf += 16;
  }

  set_bios_mode_03h();
  osd_free_bitmap( bitmap );
}


int osd_obtain_pen( unsigned char red, unsigned char green, unsigned char blue ) {
  
  /* Save old palette entries and update with new values */
  outb( first_free_pen, 0x3C7 ); /* DAC Address Read Mode Register */
  old_red[ first_free_pen ] = inb( 0x3C9 ) << 2;
  old_green[ first_free_pen ] = inb( 0x3C9 ) << 2;
  old_blue[ first_free_pen ] = inb( 0x3C9 ) << 2;

  outb( first_free_pen, 0x3C8 ); /* DAC Address Write Mode Register */
  outb( red >> 2, 0x3C9 ); /* DAC Data Register */
  outb( green >> 2, 0x3C9 );
  outb( blue >> 2, 0x3C9 );
  
  return first_free_pen++;
}


