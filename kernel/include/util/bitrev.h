/* Adopted from Linux */

#ifndef _UTIL_BITREV_H_
#define _UTIL_BITREV_H_

extern u8 const byte_rev_table[256];

static inline u8 bitrev8(u8 byte)
{
  return byte_rev_table[byte];
}

extern u16 bitrev16(u16 in);
extern u32 bitrev32(u32 in);

#endif

