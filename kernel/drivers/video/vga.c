static void dump(unsigned char *regs, unsigned count)
{
  unsigned i;

  i = 0;
  com1_printf("\t");
  for(; count != 0; count--)
    {
      com1_printf("0x%02X,", *regs);
      i++;
      if(i >= 8)
        {
          i = 0;
          com1_printf("\n\t");
        }
      else
        com1_printf(" ");
      regs++;
    }
  com1_printf("\n");
}
