static void move_cursor(int x, int y)
{
  outb (0x0E, 0x3D4);                /* CRTC Cursor location high index */
  outb ((y * 80 + x) >> 8, 0x3D5);   /* CRTC Cursor location high data */
  outb (0x0F, 0x3D4);                /* CRTC Cursor location low index */
  outb ((y * 80 + x) & 0xFF, 0x3D5); /* CRTC Cursor location low data */
}


int _putchar_with_attributes (int ch, int attribute)
{
  screen_cursor_t *cursor = get_screen_cursor();
#ifdef USE_VMX
  uint32 cpu;
  cpu = get_pcpu_id ();
#endif

  /* if backspace key */
  if(ch == 127) {
    if(cursor->x) {
      --cursor->x;
#ifdef USE_VMX
      if (shm_screen_initialized) {
        if (shm->virtual_display.cur_screen == cpu) {
          pchVideo[cursor->y * 160 + cursor->x * 2] = ' ';
          pchVideo[cursor->y * 160 + cursor->x * 2 + 1] = attribute;
          /* Move cursor */
          move_cursor(cursor->x, cursor->y);
        }
        shm_screen[cursor->y * 160 + cursor->x * 2] = ' ';
        shm_screen[cursor->y * 160 + cursor->x * 2 + 1] = attribute;
        
        shm->virtual_display.cursor[cpu].x = cursor->x;
        shm->virtual_display.cursor[cpu].y = cursor->y;
      } else {
        pchVideo[cursor->y * 160 + cursor->x * 2] = ' ';
        pchVideo[cursor->y * 160 + cursor->x * 2 + 1] = attribute;
        /* Move cursor */
        move_cursor(cursor->x, cursor->y);
      }
#else
      pchVideo[cursor->y * 160 + cursor->x * 2] = ' ';
      pchVideo[cursor->y * 160 + cursor->x * 2 + 1] = attribute;
      /* Move cursor */
      move_cursor(cursor->x, cursor->y);
#endif
    }
    return ch;
  }


  if (ch == '\n') {
    cursor->x = 0;
    cursor->y++;

    if (cursor->y > 24) {
#ifdef USE_VMX
      if (shm_screen_initialized) {
        if (shm->virtual_display.cur_screen == cpu) {
          memcpy (pchVideo, pchVideo + 160, 24 * 160);
          memset (pchVideo + (24 * 160), 0, 160);
        }
        memcpy (shm_screen, shm_screen + 160, 24 * 160);
        memset (shm_screen + (24 * 160), 0, 160);
        cursor->y = 24;
        shm->virtual_display.cursor[cpu].x = cursor->x;
        shm->virtual_display.cursor[cpu].y = cursor->y;
      } else {
        memcpy (pchVideo, pchVideo + 160, 24 * 160);
        memset (pchVideo + (24 * 160), 0, 160);
        cursor->y = 24;
      }
#else
      memcpy (pchVideo, pchVideo + 160, 24 * 160);
      memset (pchVideo + (24 * 160), 0, 160);
      cursor->y = 24;
#endif
    }
    return (int) (unsigned char) ch;
  }

  if (cursor->y * 160 + cursor->x * 2 > 80*25*2) return ch;

#ifdef USE_VMX
  if (shm_screen_initialized) {
    if (shm->virtual_display.cur_screen == cpu) {
      pchVideo[cursor->y * 160 + cursor->x * 2] = ch;
      pchVideo[cursor->y * 160 + cursor->x * 2 + 1] = attribute;
    }
    shm_screen[cursor->y * 160 + cursor->x * 2] = ch;
    shm_screen[cursor->y * 160 + cursor->x * 2 + 1] = attribute;
    cursor->x++;
    shm->virtual_display.cursor[cpu].x = cursor->x;
    shm->virtual_display.cursor[cpu].y = cursor->y;
  } else {
    pchVideo[cursor->y * 160 + cursor->x * 2] = ch;
    pchVideo[cursor->y * 160 + cursor->x * 2 + 1] = attribute;
    cursor->x++;
  }
#else
  pchVideo[cursor->y * 160 + cursor->x * 2] = ch;
  pchVideo[cursor->y * 160 + cursor->x * 2 + 1] = attribute;
  cursor->x++;
#endif

  if (cursor->y * 160 + cursor->x * 2 > 80*25*2) return ch;

#ifdef USE_VMX
  if (shm_screen_initialized) {
    if (shm->virtual_display.cur_screen == cpu) {
      pchVideo[cursor->y * 160 + cursor->x * 2] = ' ';
      pchVideo[cursor->y * 160 + cursor->x * 2 + 1] = attribute;
      shm_screen[cursor->y * 160 + cursor->x * 2] = ' ';
      shm_screen[cursor->y * 160 + cursor->x * 2 + 1] = attribute;
    }
    shm->virtual_display.cursor[cpu].x = cursor->x;
    shm->virtual_display.cursor[cpu].y = cursor->y;
  } else {
    pchVideo[cursor->y * 160 + cursor->x * 2] = ' ';
    pchVideo[cursor->y * 160 + cursor->x * 2 + 1] = attribute;
  }
#else
  pchVideo[cursor->y * 160 + cursor->x * 2] = ' ';
  pchVideo[cursor->y * 160 + cursor->x * 2 + 1] = attribute;
#endif

#ifdef USE_VMX
  if (shm_screen_initialized) {
    if (shm->virtual_display.cur_screen == cpu) {
      /* Move cursor */
      move_cursor(cursor->x, cursor->y);
    }
  } else {
    /* Move cursor */
    move_cursor(cursor->x, cursor->y);
  }
#else
  /* Move cursor */
  move_cursor(cursor->x, cursor->y);
#endif

  return (int) (unsigned char) ch;
}




